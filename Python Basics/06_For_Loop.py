array = [1,7,5,6,8,1,34,7,2] # list

# a basic for loop
for item in array:  
    print(item)

# for loop with a range function
for x in range(1,10):
    print('Number',x)
for x in range(1,10):
    print('Number',x)

# for loop with a range function and Range step = 2
for y in range(1,10,2):
    print('Num',y)

# nested for loop
for i in range(1,5):    
    for j in range(1,5):
        print("[{0},{1}]".format(i,j))

